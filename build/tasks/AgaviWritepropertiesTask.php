require_once(dirname(__FILE__) . '/AgaviTask.php');
require_once('phing/system/util/Properties.php');

/**
 * Represents any property-holding data.
 *
 *
 * @package    agavi
 * @subpackage build
 *
 * @author     Noah Fontes <noah.fontes@bitextender.com>
 * @copyright  Authors
 * @copyright  The Agavi Project
 *
 * @since      1.0.0
 *
 * @version    $Id$
 */
abstract class AgaviWritepropertiesProperties extends DataType
{
	/**
	 * Retrieves the new property or properties.
	 *
	 * @return     Properties The list of properties.
	 */
	abstract public function resolve();
}

/**
 * Represents a property reference internally for this task.
 *
 * @package    agavi
 * @subpackage build
 *
 * @author     Noah Fontes <noah.fontes@bitextender.com>
 * @copyright  Authors
 * @copyright  The Agavi Project
 *
 * @since      1.0.0
 *
 * @version    $Id$
 */
class AgaviWritepropertiesProperty extends AgaviWritepropertiesProperties
{
	protected $name = null;
	protected $value = null;
	
	/**
	 * Sets the name of the property.
	 *
	 * @param      string The name of the property.
	 */
	public function setName($name)
	{
		$this->name = (string)$name;
	}
	
	/**
	 * Sets the value of the property.
	 *
	 * @param      mixed The property value.
	 */
	public function setValue($value)
	{
		$this->value = $value;
	}
	
	/**
	 * Retrieves the new property or properties.
	 *
	 * @return     Properties The list of properties.
	 */
	public function resolve()
	{
		if($this->name === null) {
			throw new BuildException('The name attribute must be specified');
		}
		if($this->value === null) {
			throw new BuildException('The value attribute must be specified');
		}
		
		$properties = new Properties();
		$properties->setProperty($this->name, $this->value);
		return $properties;
	}
}

/**
 * Writes properties to a given file.
 *
 * @package    agavi
 * @subpackage build
 *
 * @author     Noah Fontes <noah.fontes@bitextender.com>
 * @copyright  Authors
 * @copyright  The Agavi Project
 *
 * @since      1.0.0
 *
 * @version    $Id$
 */
class AgaviWritepropertiesTask extends AgaviTask
{
	protected $file = null;
	protected $update = false;
	protected $properties = array();

	/**
	 * Sets the file that this property will modify.
	 *
	 * @param      PhingFile The file to modify.
	 */
	public function setFile(PhingFile $file)
	{
		$this->file = $file;
	}
	
	/**
	 * Sets whether this task will overwrite or update the file.
	 *
	 * @param      boolean Whether the file should be updated or replaced.
	 */
	public function setUpdate($update)
	{
		$this->update = (bool)$update;
	}
	
	/**
	 * Creates a new property to be added to the property list.
	 */
	public function createProperty()
	{
		$property = new AgaviWritepropertiesProperty();
		$this->properties[] = $property;
		return $property;
	}
	
	/**
	 * Executes the task.
	 */
	public function main()
	{
		if($this->file === null)
		{
			throw new BuildException('The file attribute must be set');
		}
		
		$properties = new Properties();
		if($this->update === true)
		{
			/* Load existing properties. */
			try {
				$properties->load($this->file);
			}
			catch(IOException $ioe) {
				/* File doesn't exist or isn't readable, so don't worry here. */
			}
		}
		
		/* Add new properties. */
		foreach($this->properties as $property)
		{
			foreach($property->resolve()->getProperties() as $name => $value)
			{
				$properties->setProperty($name, $value);
			}
		}
		
		$properties->store($this->file, 'Automatically-updated properties file ' . 
			'generated by the Agavi write-properties task');
	}
}

